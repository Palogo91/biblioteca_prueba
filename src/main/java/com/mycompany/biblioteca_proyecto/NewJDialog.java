    private void ACEPTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACEPTARActionPerformed
               
       
       String nombre=jTextField1.getText();
       String apellidos=jTextField2.getText();
       String dni=jTextField3.getText();
       String domicilio=jTextField4.getText();
       String telefono=jTextField5.getText();
       String [] todosDatos={nombre, apellidos, dni, domicilio, telefono};
        
       if(nombre.equals("")||apellidos.equals("")||dni.equals("")||domicilio.equals("")|| 
          telefono.equals("")){
           JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos", "Atencion",JOptionPane.YES_OPTION);
       }else{
          
           biblioteca.tabla1.addRow(todosDatos);
            this.setVisible(false);
       }
       
       try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3308/biblioteca", "root", "");
            con.setAutoCommit(false);
            System.out.println("Conectado");
            //Crear una sentencia de insercion de SQL
            String insertQuery="INSERT INTO usuarios SET nombre=?, apellidos=?, dni=?, domicilio=?, telefono=?";
            //Preparar la sentencia
            PreparedStatement preparedStatement=con.prepareStatement(insertQuery);
            preparedStatement.setString(1, nombre);
            preparedStatement.setString(2,apellidos);
            preparedStatement.setString(3, dni);
            preparedStatement.setString(4, domicilio);
            preparedStatement.setString(5, telefono);
            //Ejecutar la insercion
            int rowCount=preparedStatement.executeUpdate();
            con.commit();
            //Cerrar la conexion y liberar espacio
            preparedStatement.close();
            con.close();
            System.out.println("Usuario a√±adido correctamente: " + rowCount);
            
        }catch(Exception ex){
            System.out.println("No conectado o error al insertar datos. Error: ");
            ex.printStackTrace();
        }
       
      
              
       
    }//GEN-LAST:event_ACEPTARActionPerformed

